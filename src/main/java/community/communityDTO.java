package community;

public class communityDTO {
	
	private int co_id;
	private String co_title;
	private String co_date;
	private String co_author;
	private String co_content;
	
	public int getCo_id() {
		return co_id;
	}
	public void setCo_id(int co_id) {
		this.co_id = co_id;
	}
	public String getCo_title() {
		return co_title;
	}
	public void setCo_title(String co_title) {
		this.co_title = co_title;
	}
	public String getCo_date() {
		return co_date;
	}
	public void setCo_date(String co_date) {
		this.co_date = co_date;
	}
	public String getCo_author() {
		return co_author;
	}
	public void setCo_author(String co_author) {
		this.co_author = co_author;
	}
	public String getCo_content() {
		return co_content;
	}
	public void setCo_content(String co_content) {
		this.co_content = co_content;
	}
}
